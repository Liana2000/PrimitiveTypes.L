package New;

public class ForWhile {
    public static void main(String[] args) {
        for (int i = 2; i < 8; i++) {
            System.out.println("i is : " + i);
            int y = 2;
            while (y < 8) {
                y++;
                System.out.println("y is: " + y);

            }
        }
    }
}
