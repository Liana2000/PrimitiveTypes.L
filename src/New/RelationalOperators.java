package New;

public class RelationalOperators {
    public static void main(String[] args) {
        int x=34;
        int y=60;
        System.out.println(x<y);
//with cast
        int w=99;
        float z=34.5f;
        int r=(int)z;
        System.out.println(w>r);

        byte b=13;
        byte c=45;
        System.out.println(b!=c);
        long l=4567;
        long f=4567;
        System.out.println(l>=f);
        System.out.println(l<=f);

    }
}
